def add_numbers(x,y):
    """
    This is the function that counts the sum of two integers
    :param x: the first integer
    :param y:the second integer
    :return: the sum value
    """
    sum = x + y
    return sum


def is_eight(x):
    """
    This function gives a judgement of whether the parameter is 8.
    :param x: the number being checked
    :return: a boolean expression of whether the parameter x is 8
    """
    return x == 8


def triple_string(x):
    """
    This function returns the triple repetition of the parameter x
    :param x: the string being repeated
    :return: the triple repetition of parameter x
    """
    return x*3

